    public void agregarComponente() {
        DefaultTableModel model = (DefaultTableModel) tbl_entrega.getModel();
        for (Devolucion t : ctl_devolucion.getListado()) {
            Object rowData[] = {t.getId(), t.getArticuloDevolucion().getNombre(), t.getArticuloEnvio().getNombre(), t.getEntrega().getId(), t.getEntrega().getEstado()};
            model.addRow(rowData);
        }
    }

